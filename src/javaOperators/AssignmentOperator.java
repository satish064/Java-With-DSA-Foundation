package javaOperators;

public class AssignmentOperator {
    public static void main(String[] args){
        int p = 10;
        int q;

        q = p;//10
        System.out.println(q);

        p+=q;//20
        System.out.println(p);

        p-=q;//10
        System.out.println(p);

        p*=q;//100
        System.out.println(p);

        p/=q;//10
        System.out.println(p);

    }
}
