package javaOperators;

public class RelationalOperator {
    public static void main(String[] args){
        int p = 5, q = 7;

        System.out.println(p==q);//false
        System.out.println(p!=q);//true
        System.out.println(p>q);//false
        System.out.println(p<q);//true
        System.out.println(p>=q);//false
        System.out.println(p<=q);//true
    }
}
